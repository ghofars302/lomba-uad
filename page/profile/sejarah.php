                    <p>SMA Negeri I Pringsewu dididrikan melalui perjalanan panjang.&nbsp;Dalam rangka mengembangkan pendidikan di Pringsewu khususnya sekolah lanjutan yang berstatus negeri terbentuk panitia tanggal 16 April 1960. Susunan panitia sebagai berikut :</p>

                    <p>1. Pelindung: Komandan BODM ( Usman ), Kepala Pol. Distrik ( A. Hifnie )<br>2. Ketua : Wedana (R. Arifin Karta Prawira), Wakil Ketua : Camat Pringsewu (Hasan Anwar)<br>3. Sekretaris I: Penerangan ( Soegito ), Sekretaris II : Kepala Negeri ( Ngatimin )<br>4. Bendahara I: Pengawas Pasar ( Ms. Madijo Sastro ), Bendahara II: Kepala Kampung   (K.Mardiwiyato )</p>

                    <p>Langkah pertama mendidirikan SMP yang diberi nama Persiapan Negeri Pringswewu yang disahkan menjadi SMP Negeri pada tanggal 16 Januari 1962 kemudian dilanjutkan mendirirkan SMA 17 Agustus 1964 yang diberi nama SMA Negeri Persiapan.Tahun pertama jumlah siswa 45 orang jumlah guru tetap 3 orang dan 1 orang tenaga TU yang tercantum di bawah ini:<br>Drs. Mulyadi ( Kepala Sekolah), Sugiarti, Dr. Oei Kok Kiong ( Guru Tetap), Sukarmianto, Siti Asnah, B.A., Suwarno, Karsono, M. Khamndani, Sutarno,, Mas Said, Sutono ( Guru tidak tetap).Tempat belajar sementara di Gedung Wanita karena tidak cukup menggunakan pendopo Kawedanan. Tahun 1966 kebetulan Baperki diserahkan ke Pemerintah setempat yang digunakan untuk gedung SMA tersebut. Berdasarkan SK Menteri Pendidikan dan Kebudayaan RI No. 06/SK/B.III/1966 disahkan menjadi SMA Negeri dan pindah menempati gadung baru dengan jumlah lokal belajar 4 ruang lengkap dengan mobiler.</p>
                
                    <p>Sejak tahun 1964, sekolah ini telah mengalami 15 kali pergantian kepala sekolah. Nama-nama kepala sekolah sejak tahun 1964 adalah sebagai berikut</p>

